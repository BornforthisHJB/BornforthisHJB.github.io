<p>那什么是编程思维呢？编程思维就是一种把一个问题<strong>转化</strong>成另一个问题，并且把它解决掉的思考方式。这么说有些抽象，我给你举个例子吧。</p>
<p>你在数学课上，已经学过<strong>面积公式</strong>了吧？你可以通过公式，来计算长方形、三角形和圆形的面积。那如果现在我在纸上画了一只大熊猫，然后请你计算一下，这只<strong>大熊猫所占的面积</strong>，那又该怎么办呢？</p>
<p><img src="@source/column/Programmingthinking/README.assets/2b44613de5952e977b191e5d52224fc1.jpg" alt="img" loading="lazy"></p>
<p>你可能会问了，秦老师，那有没有求大熊猫面积的公式啊？哈哈，这样的公式可不存在哦。你要想知道大熊猫所占的面积，就需要利用编程思维，把这个问题，<strong>转化</strong>成另一个问题——你可以想象一下，如果在一张纸上先画出这只大熊猫，然后再在纸上<strong>均匀地撒一层大米粒</strong>。那么，大熊猫图形范围里的米粒数，跟它所占的面积是有关的。</p>
<p>比如说吧，如果这只大熊猫的面积，是这张纸全部面积的一半，那么大熊猫上面<strong>覆盖</strong>的那一层米粒，数量也应该是所有米粒的一半。</p>
<p>这时候，我们就把求大熊猫面积的这个问题，<strong>转化</strong>成了弄清楚大熊猫的图案上面，覆盖了多少米粒。再数一下这些米粒，跟纸上其他地方的米粒数<strong>比一下</strong>，再算一下这张纸的面积，这样，即使没有求大熊猫面积的公式，我们也能计算出大熊猫的面积了。</p>
<p>你看，这就是编程思维当中的一个核心思想——<strong>问题转化思想</strong>。而我给你举的这个例子，也是一种特别常见的编程算法，叫做<strong>蒙特卡洛算法</strong>。你可能听说过，有一个很厉害的人工智能程序叫 <strong>AlphaGo</strong>，下围棋胜过了人类当中最厉害的棋手。AlphaGo 的思考方法，就跟蒙特卡洛算法有关。</p>
