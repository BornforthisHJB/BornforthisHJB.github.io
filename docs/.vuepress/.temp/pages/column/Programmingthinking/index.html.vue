<p>你好，欢迎来到<strong>秦曾昌</strong>老师的“<strong>编程思维课</strong>”。</p>
<p>一说起编程，你可能最先想到的是程序员坐在电脑前面<strong>敲代码</strong>的样子。确实，这就是一般人对“编程”的想象。不过，我要告诉你的是，编程最重要的其实并不是写出那一行行代码，而是“<strong>编程思维</strong>”。</p>
