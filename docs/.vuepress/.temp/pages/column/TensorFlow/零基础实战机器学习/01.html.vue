<p>聚类、分类、回归 分类和回归的区别：</p>
<p>1、分类的标签是【离散值】；回归的标签的【连续值】；</p>
<p>2、分类实际是找到一个决策面，达到分类的目的；回归实际是找到最优拟合；</p>
<p>3、分类和回归模型的评估方法不同；</p>
</blockquote>
<blockquote>
<p>一，机器学习的概念：</p>
<p>机器学习是一种从大量的数据中生成规则、发现模型，来帮助我们预测、判断、分组和解决问题的技术。 传统程序是程序员来定义函数，而在机器学习中是机器训练出函数。机器学习的本质特征，就是从数据中发现规则。 我们做机器学习项目，就是要选定一个算法，然后用数据训练机器，找到一族函数中最适合的那一个，形成模型。</p>
<p>二，机器学习的分类：</p>
<p>训练数据集全部有标签，叫监督学习（supervised learning）； 训练数据集没有标签，叫做无监督学习（unsupervised learning）； 在训练数据集中，有的数据有标签，有的数据没有标签，我们叫做半监督学习（semi-supervised learning） 强化学习，没有训练数据集，需要从环境给它的奖惩中进行学习。</p>
<p>三，监督学习的分类：</p>
<p>根据标签的特点，监督学习可以被分为两类：回归问题（标签连续数值）和分类问题（标签离散数值）。</p>
<p>四，深度学习：</p>
<p>其深度学习是一种使用深层神经网络算法的机器学习模型，可以应用在监督学习、半监督学习和无监督学习和强化学习中。 深层神经网络的厉害之处在于，它能对非结构的数据集进行自动的复杂特征提取，完全不需要人工干预。在视觉处理和自然语言处理方面都很厉害。</p>
</blockquote>
