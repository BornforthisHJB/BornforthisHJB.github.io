<h2 id="思考题" tabindex="-1"><a class="header-anchor" href="#思考题" aria-hidden="true">#</a> 思考题</h2>
<p>这节课就到这里了，最后，我想给你留三个思考题，假如你有一个网店：</p>
<ol>
<li>你有用户的历史订单数据，你想要根据这些数据给用户分组，这是什么类型的机器学习问题？</li>
<li>你有用户的人口统计数据，比如年龄、性别、城市和年收入等等，并且你已经把这些用户已经分成了高、中、低三个价值组。这时拉新团队给了你一批新用户信息，想要判断新用户会属于哪一组，这是什么类型的机器学习问题？</li>
<li>你有用户的人口统计信息和购买历史记录，你想要预测新注册的用户的预期消费总额，这是什么类型的机器学习问题？</li>
</ol>
<p>提示：先思考是有监督还是无监督学习，如果你认为是监督学习，进一步想想是回归问题、还是分类问题。</p>
