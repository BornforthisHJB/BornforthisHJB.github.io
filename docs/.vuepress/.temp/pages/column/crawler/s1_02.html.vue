这样大家就开始都变成那种讨人厌的样子了，毕竟那样讨人厌，但是能赚钱呀。谁不希望自己的付出能有一点回报呢？所以 ，我希望我的这个课程呢，能改变一下这个观念，也能然这种情况有所好转，哪怕只是一点点，所以希望大家能多多支持优质内容。</p>
</blockquote>
