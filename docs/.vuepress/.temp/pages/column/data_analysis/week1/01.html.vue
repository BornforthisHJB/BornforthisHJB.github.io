<h2 id="数据分析师-必须掌握的技能" tabindex="-1"><a class="header-anchor" href="#数据分析师-必须掌握的技能" aria-hidden="true">#</a> 数据分析师，必须掌握的技能</h2>
<p>数据分析技能主要分两类：</p>
<ul>
<li>硬技能</li>
<li>软技能</li>
</ul>
<p><img src="@source/column/data_analysis/week1/01.assets/image-20220806221219186.png" alt="image-20220806221219186" loading="lazy"></p>
<p><strong>什么是硬技能呢？</strong></p>
<p>——硬性的数据处理和分析工具的使用。</p>
<p>比如：</p>
<ul>
<li>电子表格 Excel</li>
<li>数据库操作语言 Sql</li>
<li>可视化面板 Tableau</li>
<li>大数据处理分析 Python</li>
</ul>
<p>那这些工具，你会就是会，不会就是不会。很容易识别。所以，经常作为数据分析的硬性门槛，是你必须要掌握的技能。</p>
<p><strong>那什么是软技能呢？</strong></p>
<p>——对事物的认知方式，以及经验积累</p>
<p><img src="@source/column/data_analysis/week1/01.assets/image-20220806221441666.png" alt="image-20220806221441666" loading="lazy"></p>
<p>比如：给你一组股票价格的走势图， 深蓝色的代表股票 A，浅蓝色的代表股票 B。</p>
<p>那对于不太了解股票的人（小猿），你能做出的判断就是：<strong>A 的价格比 B 要高</strong> ；</p>
<p>但对于有投资经验的（小悦），会分析出：<strong>股票 A 的市值比 B 高，所以价格才会对应的高</strong>；</p>
<p>还可以通过它们的波动程度，分析出：<strong>A 的波动率是大于 B 的，而波动率也意味着风险程度，也就是说  A 的风险大于 B 的。</strong></p>
<p>那显然 小悦的软技能，也就是认知水平，思维水平，是高于 小猿 的。这个是由他的经验决定。</p>
<p>再来一个人 Austin，Austin 和 小悦一样，都是有投资经验的，Austin 知道每一个节点产生的背景，也就是说：<strong>行情下跌，它是由于某个事件，或者某一个政策导致的；上涨，它又是由于某一个事件或者政策导致的。</strong></p>
<p>那么，Austin 的软技能会比 小悦、小猿更高。因为，他具有更宏观的思维方式与框架，而以上所有提到的硬技能、软技能我都将会交给你。</p>
