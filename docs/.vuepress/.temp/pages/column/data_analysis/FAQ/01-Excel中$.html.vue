<li>
<p><code v-pre>$A</code> 列绝对引用，行相对引用</p>
</li>
<li>
<p><code v-pre>A$1</code> 列相对引用，行绝对引用</p>
</li>
<li>
<p><code v-pre>$A$1</code> 行列绝对引用</p>
</li>
