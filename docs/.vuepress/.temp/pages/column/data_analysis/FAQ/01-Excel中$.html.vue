<h2 id="excel-里-是什么意思" tabindex="-1"><a class="header-anchor" href="#excel-里-是什么意思" aria-hidden="true">#</a> Excel 里 $是什么意思</h2>
<p><strong>两种意思：</strong></p>
<p>1、在单元格引用里，表示绝对引用。就是 $ 后边的那个字母或数字不随公式的拖动而变化。</p>
<p>2、在数据表示里，表示美元。</p>
<p>用在公式里是锁行和锁列的，$ 在公式里的数字前锁行，字母前锁列，前后都加是锁行和列，这个按 F4 就可以出来了</p>
