<h2 id="_1-什么是-html" tabindex="-1"><a class="header-anchor" href="#_1-什么是-html" aria-hidden="true">#</a> 1. 什么是 HTML</h2>
<p>HTML 称为<strong>超文本</strong>标记语言，是一种<strong>标识性</strong>的语言。它包括一系列标签。通过这些标签可以将网络上的文档格式统一，使分散的 Internet 资源连接为一个逻辑整体。</p>
<p>超文本是一种组织信息的方式，它通过超级链接方法将文本中的文字、图表与其他信息媒体相关联。这些相互关联的信息媒体可能在同一文本中，也可能是其他文件，或是地理位置相距遥远的某台计算机上的文件。这种组织信息方式将分布在不同位置的信息资源用随机方式进行连接，为人们查找，检索信息提供方便。</p>
<h2 id="_2-html-的由来" tabindex="-1"><a class="header-anchor" href="#_2-html-的由来" aria-hidden="true">#</a> 2. HTML 的由来</h2>
<p>HTML 的英文全称是 Hyper Text Marked Language，即<code v-pre>超文本标记语言</code>。</p>
<blockquote>
<p>HTML 是由 Web 的发明者 Tim Berners-Lee 和同事 Daniel W. Connolly 于 1990 年创立的一种标记语言，它是标准通用化标记语言 SGML 的应用。</p>
</blockquote>
<p>用 HTML 编写的超文本文档称为 HTML 文档，<strong>它能独立于各种操作系统平台</strong>（如 UNIX， Windows 等）。使用 HTML 语言，将所需要表达的信息按某种规则写成 HTML 文件，通过专用的浏览器来识别，并将这些 HTML 文件“翻译”成可以识别的信息，即现在所见到的网页。</p>
<h2 id="_3-与普通文本的区别" tabindex="-1"><a class="header-anchor" href="#_3-与普通文本的区别" aria-hidden="true">#</a> 3. 与普通文本的区别</h2>
<p>普通文本文件，例如后缀名为 <code v-pre>.txt</code> 的文件，只能插入文字，段落等，而 HTML 文件，后缀名为 <code v-pre>.html</code> 的文件，既可以插入文字，段落，也可以插入表格，图片，链接，音频，视频等众多资源。</p>
<p>HTML 文件中，我们可以通过 HTML 标签，来实现不同的结构排列。而大部分 HTML 标签都有特定的样式和意义，所以，我们可以用这些标签自由组合，来完成网页的基本结构。</p>
<h2 id="_4-html-的标准" tabindex="-1"><a class="header-anchor" href="#_4-html-的标准" aria-hidden="true">#</a> 4. HTML 的标准</h2>
<p>超级文本标记语言是标准通用标记语言下的一个应用，也是一种规范，一种标准，它通过标记符号来标记要显示的网页中的各个部分。</p>
<p>网页文件本身是一种文本文件，通过在文本文件中添加标记符，可以告诉浏览器如何显示其中的内容（如：文字如何处理，画面如何安排，图片如何显示等）。</p>
<p>浏览器按顺序阅读网页文件，然后根据标记符解释和显示其标记的内容，对书写出错的标记将不指出其错误，且不停止其解释执行过程，编制者只能通过显示效果来分析出错原因和出错部位。</p>
<blockquote>
<p>提示：但需要注意的是，对于不同的浏览器，对同一标记符可能会有不完全相同的解释，因而可能会有不同的显示效果。</p>
</blockquote>
<p>HTML 现在最新标准为第五代标准，又简称为 <strong>HTML5</strong> 。HTML 在 Web 迅猛发展的过程中起着重要作用，有着重要的地位。但随着网络应用的深入，特别是电子商务的应用，HTML 过于简单的缺陷很快凸现出来。HTML 不允许应用程序开发者为具体的应用环境定义自定义的标记。HTML 只能用于信息显示。 HTML 可以设置文本和图片显示方式，但没有语义结构，即 HTML 显示数据是按照布局而非语义的。</p>
<p>随着网络应用的发展，各行业对信息有着不同的需求，这些不同类型的信息未必都是以网页的形式显示出来。例如，当通过搜索引擎进行数据搜索时，按照语义而非按照布局来显示数据会具有更多的优点。</p>
<h2 id="_5-html-的特点" tabindex="-1"><a class="header-anchor" href="#_5-html-的特点" aria-hidden="true">#</a> 5. HTML 的特点</h2>
<ol>
<li><strong>简易性：</strong> 超级文本标记语言版本升级采用超集方式，从而更加灵活方便；</li>
<li><strong>可扩展性：</strong> 超级文本标记语言的广泛应用带来了加强功能，增加标识符等要求，它采取子类元素的方式，为系统扩展带来保证；</li>
<li><strong>平台无关性：</strong> 虽然个人计算机大行其道，但使用 MAC 等其他机器的大有人在，超级文本标记语言可以使用在广泛的平台上，这也是万维网（WWW）盛行的另一个原因；</li>
<li><strong>通用性：</strong> 另外，HTML 是网络的通用语言，一种简单、通用的全置标记语言。它允许网页制作人建立文本与图片相结合的复杂页面，无论使用的是什么类型的电脑或浏览器，这些页面可以被网上任何其他人浏览到。</li>
</ol>
<h2 id="_6-html-的应用" tabindex="-1"><a class="header-anchor" href="#_6-html-的应用" aria-hidden="true">#</a> 6. HTML 的应用</h2>
<p>如果我们需要编写网页，那么 HTML 为我们提供的标签，就成为了网页的结构。总结起来就是，HTML 为网页提供了基本结构。所以我们要编写网页，HTML 就成为了我们的第一课。</p>
