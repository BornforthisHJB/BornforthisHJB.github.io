<h2 id="web-怎么工作" tabindex="-1"><a class="header-anchor" href="#web-怎么工作" aria-hidden="true">#</a> Web 怎么工作？</h2>
<p><strong>我们在上网时看到的视音频、图文资源是以文件的形式存放在 Web 服务器上的。</strong></p>
<p>Web 服务器就像一台 7X24 小时持续作业的计算机，它为我们顺利上网而辛勤工作。</p>
<p>Web 服务器上的文件并不是随意堆放的。网页制作人在发布网站前，已把各类资源进行规范分类和命名——图片归入图片文件夹；样式归入样式文件夹；脚本归入脚本文件夹......</p>
<p><img src="@source/column/web/base/base_01.assets/image-20220711091602969.png" alt="image-20220711091602969" loading="lazy"></p>
<p><strong>文件夹之间是通过超文本标记语言（HTML）等建立联系的。</strong></p>
<p><img src="@source/column/web/base/base_01.assets/image-20220711091736450.png" alt="image-20220711091736450" loading="lazy"></p>
<p>各类资源之间建立了规范而合理的联系后，它们需要与服务器建立联系。</p>
<p>当我们使用浏览器访问网页时，<strong>网页浏览器就会向服务器发出请求。</strong></p>
<p><strong>服务器收到请求后</strong>，<strong>只要它能在文件中找得到对应的资源，服务器都会把资源发送给浏览器。</strong></p>
<p><img src="@source/column/web/base/base_01.assets/image-20220711091908012.png" alt="image-20220711091908012" loading="lazy"></p>
<p>网页文件的规范联系，浏览器的合理请求，服务器的正常运作使我们能够顺利在网上找到想要的资源。</p>
<p>这就是 Web 的工作。</p>
<p><strong>Web 的工作过程：</strong></p>
<ol>
<li>用户在浏览器点击资源并向服务器发出请求。</li>
<li>服务器收到请求。</li>
<li>服务器查找资源。</li>
<li>服务器返回资源给浏览器。</li>
<li>用户浏览想看的资源。</li>
</ol>
<h2 id="web-文件路径" tabindex="-1"><a class="header-anchor" href="#web-文件路径" aria-hidden="true">#</a> Web 文件路径</h2>
<p>为了使服务器快速地响应浏览器的请求，<strong>Web 文件需要给服务器提供明确的路径。</strong></p>
<p><strong>什么是 Web 文件路径呢？</strong></p>
<p>举个例子。</p>
<p>你请求室友帮你在衣柜里找一枚黑色领带夹。为了快速的响应你的需求，舍友反问你：那枚领带夹在哪儿？</p>
<p>你说：黑色领带夹在我的衣柜右边的第二个抽屉的蓝色盒子里。</p>
<p>“我的衣柜右边/第二个抽屉/蓝色盒子”是指引舍友帮你找到领带夹的路径。</p>
<p>同理，</p>
<p>当你访问 <a href="bornforthis.cn">bornforthis.cn</a> 的网页，想查找一张猫头鹰图片。你的浏览器向 Bornforthis 服务器发起了请求。</p>
<p>如果W3Cschool的网页没有将图片的路径写入文件，服务器就无法响应你的请求。</p>
<p><strong>Before: 没有图片路径</strong></p>
<p><img src="@source/column/web/base/base_01.assets/image-20220712195038841.png" alt="image-20220712195038841" loading="lazy"></p>
<p><strong>After: 图片路径明确</strong></p>
<p><img src="@source/column/web/base/base_01.assets/image-20220712222205964.png" alt="image-20220712222205964" loading="lazy"></p>
<p>Before 和 After 之间差了这条路径：<strong>img/quicksand.png</strong></p>
<p>在此路径中，“img” 和 &quot;quicksand.png&quot; 是 “<strong>父子关系</strong>”，它们之间用英文状态下的 “<strong>/</strong>” 隔开。</p>
<p>img 是存放网站所有图片的文件夹，<strong>quicksand.png</strong> 是其中一张图片。“<strong>父子关系</strong>” 因此而生。</p>
<p>【判断题】横线符号 “<strong>-</strong>” 用来分隔文件路径的父子关系。</p>
