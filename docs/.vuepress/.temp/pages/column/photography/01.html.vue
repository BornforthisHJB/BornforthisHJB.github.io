<h3 id="_2-旁轴相机" tabindex="-1"><a class="header-anchor" href="#_2-旁轴相机" aria-hidden="true">#</a> 2. 旁轴相机</h3>
<p><img src="@source/column/photography/01.assets/image-20220718172332520.png" alt="image-20220718172332520" loading="lazy"></p>
<p><img src="@source/column/photography/01.assets/image-20220718184636253.png" alt="image-20220718184636253" loading="lazy"></p>
<p><strong>优点：</strong></p>
<ol>
<li>轻便，低调；</li>
<li>噪声小；</li>
<li>对焦精密（旁轴测距仪相机）；</li>
<li>这是目前相机，在对焦上无与伦比的相机；（虽然，它只能用手动对焦模式，这个地方要注意：我不是说手动对焦是最精密的，我说的是旁轴测距仪相机的手动对焦是最精密的）</li>
</ol>
<p><strong>缺点：</strong></p>
<ol>
<li>对焦缓慢，毕竟要手动对焦嘛；</li>
<li>而且镜头能拍到的效果也是看不见的，你不知道你拍出来的是什么样子——拍摄效果不直观；</li>
<li>构图有偏差，你真正拍到的画面，会在你看到画面的，右下角；</li>
</ol>
<p>所以，基于旁轴相机画质清晰、相机又低调这个特性，我们经常用它拍摄人文摄影。</p>
<p><img src="@source/column/photography/01.assets/image-20220718184654197.png" alt="image-20220718184654197" loading="lazy"></p>
<p>在剧院、法博物馆中，单反相机的噪音又太大不让进入，但是旁轴相机可以进入。</p>
<p><img src="@source/column/photography/01.assets/image-20220718184731687.png" alt="image-20220718184731687" loading="lazy"></p>
<p>在弱光环境下，旁轴相机有可能会比单反相机来的清晰。</p>
