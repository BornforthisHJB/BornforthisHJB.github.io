<h3 id="_3-微单相机" tabindex="-1"><a class="header-anchor" href="#_3-微单相机" aria-hidden="true">#</a> 3. 微单相机</h3>
<p><img src="@source/column/photography/01.assets/image-20220718184821681.png" alt="image-20220718184821681" loading="lazy"></p>
<p><img src="@source/column/photography/01.assets/image-20220718184925910.png" alt="image-20220718184925910" loading="lazy"></p>
<p><strong>优点：</strong></p>
<ol>
<li>因为，它去掉了单反的反光镜的结构，所以它非常小巧——轻便；</li>
<li>可以更换镜头（单反也可以）；</li>
<li>配件丰富，灵活扩展；</li>
<li>价格亲民，性价比高；</li>
</ol>
<p><strong>缺点：</strong></p>
<ol>
<li>对焦速度，和连拍速度一般来说会比较慢（高端的微单相机除外）；</li>
<li>取景时滞，因为是电子取景，所以不像光学取景那样快捷；</li>
<li>当然，如果大家使用的这个微单相机的感光元件比较小的话，那么在暗光摄影的画质，可能会不如全画幅单反相机。</li>
</ol>
