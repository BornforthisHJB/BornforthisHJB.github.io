<h3 id="_1-单反相机" tabindex="-1"><a class="header-anchor" href="#_1-单反相机" aria-hidden="true">#</a> 1. 单反相机</h3>
<p><img src="@source/column/photography/01.assets/image-20220718162455005.png" alt="image-20220718162455005" loading="lazy"></p>
<p><strong>优点：</strong></p>
<ol>
<li>光学取景，反应迅速；</li>
<li>按钮很多，操作方便，功能全面；</li>
</ol>
<p><strong>缺点：</strong></p>
<ol>
<li>略显专业，太过张扬；</li>
<li>用单反相机去做新闻摄影或者纪实摄影的时候，很容易引起对方情绪上的反弹；</li>
<li>单反相机是单镜头反光相机，这相机里面有一片镜子，每按一下快门就会弹动一下——震动明显，噪音很大，拍出来的照片容易模糊。</li>
</ol>
